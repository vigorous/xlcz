package cn.gov.zjport.xlcz.domain.common;

import java.io.Serializable;

/**
 * 菜单JSON对象
 *
 * @author <a href="mailto:cjun@zjport.gov.cn">cjun</a>
 * @version $Id$
 * @since 1.0
 */
public class MenuJson implements Serializable {
    /** serialVersionUID */
    private static final long serialVersionUID = -1005270959500776941L;
    /** 菜单ID */
    private String id;
    /** 上级菜单ID */
    private String pid;
    /** 菜单名称 */
    private String text;
    /** 菜单URL */
    private String url;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the pid
     */
    public String getPid() {
        return pid;
    }

    /**
     * @param pid the pid to set
     */
    public void setPid(String pid) {
        this.pid = pid;
    }

    /**
     * @return the text
     */
    public String getText() {
        return text;
    }

    /**
     * @param text the text to set
     */
    public void setText(String text) {
        this.text = text;
    }

    /**
     * @return the url
     */
    public String getUrl() {
        return url;
    }

    /**
     * @param url the url to set
     */
    public void setUrl(String url) {
        this.url = url;
    }
}
